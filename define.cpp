// 读取预设方案文件数据(id 预设方案编号, strFilePath 读取文件路径)
bool CScheme::readDataFile(const int &id, const QString &strFilePath)
{
    // 读取预设方案数据: 3字节["FCD"] + 1字节[方案类型] + (字符串数据) * N
    QFile schemeFile(strFilePath);
    if (!schemeFile.open(QIODevice::ReadOnly)) return SetErrorInfo(-1);
    QByteArray&& fileData = schemeFile.readAll();
    if (fileData.size() < 4) return SetErrorInfo(-1);
    if (fileData.left(3) != "FCD") return SetErrorInfo(-1);

    // 初始化数据
    const char* data = fileData.data();
    const int&& dataLen = fileData.size();
    const int&& int_max{ 0x3fffffff };
    int&& index{ 3 };
    /* 是否读取到结尾 */ auto lam_isend = [&](const int& i) -> bool {
        if (i < dataLen) return false;
        if (i == dataLen) return true;
        index = int_max;
        return true;
    };
    /* 读取字符串(1字节[长度]+[字符串]) */ auto lam_rstr = [&]() -> QString {
        if (lam_isend(index)) return QString();
        const int&& nSize = (int)data[index++];
        if (lam_isend((index += nSize) - 1)) return QString();
        return QString::fromLocal8Bit(&data[index - nSize], nSize);
    };

    // 添加预设方案
    m_schemeList.insert(id, { { id / 10, id % 10 }, data[index++], {}, "" });
    SSchemeData& scheme = m_schemeList[id];
    // 读取 预设方案名[0][0] + 一级面板[0][1~9] + 二级面板[1~9][0~9]
    for (int&& i{ 0 }; i <= 9; ++i) for (int&& j{ 0 }; j <= 9; ++j) scheme[i][j] = lam_rstr();
    // 更新拼写
    scheme.updateSpell();

    if (index != int_max) return true;
    m_schemeList.remove(id);
    return false;
}
