SSchemeData &CScheme::append(const int &id)
{
    auto& scheme = m_schemeList[id];
    scheme.id[0] = id / 10;
    scheme.id[1] = id % 10;
    scheme.type = 0;
    scheme[0][0] = QString::fromLocal8Bit("方案名称");
    scheme[0][1] = QString::fromLocal8Bit("在此处填写快捷发言的内容");
    scheme[0][2] = QString::fromLocal8Bit("启用二级面板后，此页面用作索引使用，实际发送文字填在二级面板内");
    update(scheme);
    return scheme;
}

