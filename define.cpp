// 初始化,读取预设方案配置(strDirPath 储存数据目录)
bool CScheme::init(const QString &strDirPath)
{
    m_schemeList.clear();
    m_strDirPath = strDirPath;

    /* 读取文件全部数据 */ auto lam_rfile = [](const QString& strFilePath) -> QByteArray {
        QFile inFile(strFilePath);
        if (!inFile.open(QIODevice::ReadOnly)) return QByteArray();
        QByteArray&& fileData = inFile.readAll();
        inFile.close();
        return std::move(fileData);
    };

    // 读取索引文件
    QVector<int> idList;
    {
        // 索引文件结构: 8字节["FCDINDEX"] + 100字节[对应00~99编号，值为1时此编号存在，否则空，00编号为保留值]
        QByteArray&& fileData = lam_rfile(m_strDirPath + "/fcdSchemeIndex");
        if (fileData.size() < 108) return SetErrorInfo(-1);
        if (fileData.left(8) != "FCDINDEX") return SetErrorInfo(-1);
        for (int&& i{ 1 }; i <= 99; ++i) if (fileData.at(i + 8)) idList.push_back(i);
    }

    // 读取预设方案
    for (auto& id : idList) readDataFile(id, m_strDirPath + "/data" + QString::number(id / 10) + QString::number(id % 10) + ".fcdscheme");
    return true;
}

// 更新 预设方案(id 需要更新的预设方案编号)
bool CScheme::update(const int &id) const
{
    return update(m_schemeList[id]);
}

// 更新 预设方案(scheme 需要更新的预设方案)
bool CScheme::update(const SSchemeData &scheme) const
{
    QFile indexFile(m_strDirPath + "/fcdSchemeIndex");
    if (!indexFile.open(QIODevice::ReadWrite)) return SetErrorInfo(-1);
    indexFile.seek(8 + scheme.id[0] * 10 + scheme.id[1]);
    indexFile.putChar(1);
    indexFile.close();

    QFile schemeFile(m_strDirPath + "/data" + QString::number(scheme.id[0]) + QString::number(scheme.id[1]) + ".fcdscheme");
    if (!schemeFile.open(QIODevice::WriteOnly)) return SetErrorInfo(-1);
    /* 写入字符串(1字节[长度]+[字符串]) */ auto lam_wstr = [&](const QString& str) {
        QByteArray&& data = str.toLocal8Bit();
        schemeFile.putChar(data.size());
        schemeFile.write(data);
    };
    schemeFile.write("FCD", 3);
    schemeFile.putChar(scheme.type);
    for (int&& i{ 0 }; i <= 9; ++i) for (int&& j{ 0 }; j <= 9; ++j) lam_wstr(scheme[i][j]);
    schemeFile.close();
    return true;
}

// 导入 预设方案(strFilePath 导入文件路径)
int CScheme::import(const QString &strFilePath)
{
    // 获取预设方案编号
    int&& id{ 0 };
    while (id <= 99) if (!m_schemeList.contains(++id)) break;
    if (id > 99) return 0;
    // 读取预设方案数据
    if (!readDataFile(id, strFilePath)) return SetErrorInfo(GetErrorInfo());
    // 更新预设方案数据
    return update(id) ? id : 0;
}

// 导出 预设方案(strFilePath 导出文件路径, id 需要导出的预设方案编号)
bool CScheme::output(const QString &strFilePath, const int& id) const
{
    QFile schemeFile(strFilePath);
    if (!schemeFile.open(QIODevice::WriteOnly)) return SetErrorInfo(-1);
    /* 写入字符串(1字节[长度]+[字符串]) */ auto lam_wstr = [&](const QString& str) {
        QByteArray&& data = str.toLocal8Bit();
        schemeFile.putChar(data.size());
        schemeFile.write(data);
    };
    schemeFile.write("FCD", 3);
    schemeFile.putChar(m_schemeList[id].type);
    for (int&& i{ 0 }; i <= 9; ++i) for (int&& j{ 0 }; j <= 9; ++j) lam_wstr(m_schemeList[id][i][j]);
    schemeFile.close();
    return true;
}

// 重命名 预设方案(oldid 旧方案编号, newid 新方案编号)
bool CScheme::rename(const int &oldid, const int &newid)
{
    // 更改索引文件信息
    QFile indexFile(m_strDirPath + "/fcdSchemeIndex");
    if (!indexFile.open(QIODevice::ReadWrite)) return SetErrorInfo(-1);
    indexFile.seek(8 + oldid);
    indexFile.putChar(0);
    indexFile.seek(8 + newid);
    indexFile.putChar(1);
    indexFile.close();

    // 重命名数据存储文件名
    const QString&& strOldFile = m_strDirPath + "/data" + ToIdStr(oldid) + ".fcdscheme";
    const QString&& strNewFile = m_strDirPath + "/data" + ToIdStr(newid) + ".fcdscheme";
    if (!QFile::rename(strOldFile, strNewFile))
    {
        if (QFile::exists(strNewFile)) QFile::setPermissions(strNewFile, QFileDevice::ReadOwner | QFileDevice::WriteOwner), QFile::remove(strNewFile);
        QFile::setPermissions(strOldFile, QFileDevice::ReadOwner | QFileDevice::WriteOwner);
        QFile::rename(strOldFile, strNewFile);
    }

    // 移动列表数据
    m_schemeList.insert(newid, {});
    std::swap(m_schemeList[newid], m_schemeList[oldid]);
    m_schemeList[newid].id[0] = newid / 10;
    m_schemeList[newid].id[1] = newid % 10;
    m_schemeList.remove(oldid);

    return true;
}

// 删除 预设方案(id 需要删除的预设方案编号)
bool CScheme::remove(const int &id)
{
    // 删除索引文件信息
    QFile indexFile(m_strDirPath + "/fcdSchemeIndex");
    if (!indexFile.open(QIODevice::ReadWrite)) return SetErrorInfo(-1);
    indexFile.seek(8 + id);
    indexFile.putChar(0);
    indexFile.close();

    // 删除数据存储文件
    const QString&& strDataFile = m_strDirPath + "/data" + QString::number(id / 10) + QString::number(id % 10) + ".fcdscheme";
    if (!QFile::remove(strDataFile))
        if (QFile::exists(strDataFile))
            QFile::setPermissions(strDataFile, QFileDevice::ReadOwner | QFileDevice::WriteOwner), QFile::remove(strDataFile);

    // 删除列表数据
    m_schemeList.remove(id);
    return true;
}

